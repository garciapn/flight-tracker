def get_top_10_flights():
    """Get top 15 flights from real scraped data with historical pricing"""
    flights = get_flight_data()[:15]  # Get top 15
    
    # Enrich each flight with historical data
    for flight in flights:
        airline = flight.get('airline', '')
        # Handle both int and string price formats
        price_val = flight.get('price', 0)
        if isinstance(price_val, str):
            current_price_str = price_val.replace('$', '').replace(',', '')
            try:
                current_price = float(current_price_str)
            except:
                current_price = 0
        else:
            current_price = float(price_val)
        
        # Get historical prices for this airline
        hist_prices = get_flight_history_for_airline(airline)
        
        if hist_prices:
            avg_price = sum(hist_prices) / len(hist_prices)
            pct_change = ((current_price - avg_price) / avg_price * 100) if avg_price > 0 else 0
            
            flight['avg_historical_price'] = f"${avg_price:.0f}"
            flight['pct_change'] = f"{pct_change:+.1f}%"
            flight['price_color'] = "green" if pct_change < 0 else "red"
        else:
            flight['avg_historical_price'] = "N/A"
            flight['pct_change'] = "N/A"
            flight['price_color'] = "gray"
    
    return flights

