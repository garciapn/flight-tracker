def get_price_trends():
    """Get price trends from daily JSON files (last 7 days)"""
    trends = []
    
    if not os.path.exists(DATA_PATH):
        return []
    
    try:
        # Scan all date files in data directory
        for fname in sorted(os.listdir(DATA_PATH)):
            if fname.endswith('.json') and re.match(r'\d{4}-\d{2}-\d{2}', fname.replace('.json', '')):
                file_path = os.path.join(DATA_PATH, fname)
                with open(file_path) as f:
                    data = json.load(f)
                    
                timestamp = data.get('timestamp', '')
                stats = data.get('stats', {})
                
                # If file doesn't have stats, calculate from flights
                if not stats or not stats.get('min'):
                    flights = data.get('flights', [])
                    prices = []
                    for flight in flights:
                        try:
                            # Try to get price - handle both price and raw_price_per_person fields
                            price = flight.get('price')
                            if not price:
                                price = flight.get('raw_price_per_person')
                            
                            if isinstance(price, str):
                                price = int(price.replace('$', '').replace(',', ''))
                            elif isinstance(price, (int, float)):
                                price = int(price)
                            
                            if price and price > 0:
                                prices.append(price)
                        except:
                            pass
                    
                    if prices:
                        # Detect if prices are per-person or round-trip
                        # Round-trip prices are typically 1.5x-2x per-person for same airlines
                        # If min > 2000, likely already round-trip (should be normalized to per-person)
                        avg_price = sum(prices) / len(prices)
                        is_round_trip = avg_price > 1500  # Heuristic: round-trip prices > $1500
                        
                        if is_round_trip:
                            # Divide by 2 to get per-person
                            prices = [p / 2 for p in prices]
                        
                        stats = {
                            'min': min(prices),
                            'max': max(prices),
                            'avg': sum(prices) / len(prices),
                            'count': len(prices)
                        }
                
                # Parse timestamp to get date
                try:
                    dt = datetime.fromisoformat(timestamp.replace('Z', '+00:00'))
                    date_str = dt.strftime('%Y-%m-%d')
                except:
                    date_str = fname.replace('.json', '')
                
                # Store both min (for 2 passengers) and per-person for consistency
                trends.append({
                    "date": date_str,
                    "best_price": stats.get('min'),
                    "average_price": stats.get('avg'),
                    "max_price": stats.get('max'),
                    "count": stats.get('count', 0)
                })
    except Exception as e:
        print(f"Error reading trends: {e}")
    
    # Keep only unique dates, using the latest entry per date
    seen = {}
    for t in reversed(trends):
        if t['date'] not in seen:
            seen[t['date']] = t
    
    return list(reversed(list(seen.values())))  # All available data (no limit)

def get_price_alerts():
    """Get recent price alerts from alerts log"""
    alerts_file = os.path.join(BASE_PATH, "alerts", "price-drops.log")
    alerts = []
    
    if os.path.exists(alerts_file):
        try:
            with open(alerts_file) as f:
                for line in f.readlines()[-5:]:  # Last 5 alerts
                    if line.strip():
                        # Parse log format: timestamp | DROP: -$X (-Y%) | was → now
                        parts = line.split('|')
                        if len(parts) >= 2:
                            alerts.append({
                                "timestamp": parts[0].strip(),
                                "alert": parts[1].strip() if len(parts) > 1 else "Price drop detected"
                            })
        except:
            pass
    
    return alerts

