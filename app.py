@app.route('/api/real-prices')
def real_prices():
    """Get real booking prices from all platforms"""
    try:
        import subprocess
        import json
        
        result = subprocess.run(
            ["python3", os.path.join(BASE_PATH, "price-scraper.py")],
            capture_output=True,
            text=True,
            timeout=120
        )
        
        # Extract JSON from output (find first { and last })
        output = result.stdout
        start = output.find('{')
        end = output.rfind('}') + 1
        
        if start >= 0 and end > start:
            json_str = output[start:end]
            prices = json.loads(json_str)
            return jsonify(prices)
        else:
            raise ValueError("No JSON found in output")
    
    except Exception as e:
        return jsonify({
            "error": str(e),
            "message": "Could not fetch real prices"
        }), 500

